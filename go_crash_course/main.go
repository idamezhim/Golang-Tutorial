package main

import "fmt"

func main() {
// declare variables
var str string
var n, m int
var mn float32
// assign values
str = "Hello World"
n = 10
m = 50
mn = 2.45
fmt.Println(str)
fmt.Println(n)
fmt.Println(m)
fmt.Println(mn)
// declare and assign values to variables
var city string = "London"
var x int = 100
fmt.Println(city)
fmt.Println(x)
// declare variable with defining its type
country := "DE"
val := 15
fmt.Println(country)
fmt.Println(val)
// define multiple variables
var (
name string
email string
age int
)
name = "john"
email = "john@email.com"
age = 27
fmt.Println(name)
fmt.Println(email)
fmt.Println(age)
}
